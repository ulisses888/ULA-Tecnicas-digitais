LIBRARY IEEE;
use IEEE.std_logic_1164.all;

Entity operacao_OR IS
PORT (

a,b :in std_logic_vector(3 downto 0);
s :out std_logic_vector (3 downto 0)
);

end operacao_OR;
architecture arq_operacao_OR of operacao_OR is
begin

s(0) <= a(0) or b(0);
s(1) <= a(1) or b(1);
s(2) <= a(2) or b(2);
s(3) <= a(3) or b(3);

end arq_operacao_OR;